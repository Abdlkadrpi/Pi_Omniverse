#!/bin/bash

# 1. طلب الرمز من القائد
echo "------------------------------------------"
echo "🚀 مرحباً بك في نظام تفعيل أومني فيرس (Web3)"
echo "------------------------------------------"
read -p "🔑 يرجى لصق الـ Authtoken الخاص بك هنا: " MY_TOKEN

if [ -z "$MY_TOKEN" ]; then
    echo "❌ خطأ: لم تقم بإدخال الرمز!"
    exit 1
fi

# 2. تفعيل الرمز في نسخة ngrok المحلية
echo "⚙️ جاري ضبط الإعدادات..."
./ngrok.exe config add-authtoken $MY_TOKEN

# 3. تشغيل النفق العالمي
echo "🌐 جاري فتح النفق على المنفذ 5000..."
echo "💡 (بمجرد ظهور الرابط، انسخه وافتح نافذة ثانية لتشغيل auto_sync.py)"
echo "------------------------------------------"
./ngrok.exe http 5000